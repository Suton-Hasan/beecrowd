A, B, C = list(map(float, input().split()))
pi=3.14159
TRIANGULO=(1/2)*A*C
CIRCULO=C*C*pi
TRAPEZIO=(A+B)/2*C
QUADRADO=B*B
DISHA=A*B
print("TRIANGULO: %0.3f"%TRIANGULO)
print("CIRCULO: %0.3f"%CIRCULO)
print("TRAPEZIO: %0.3f"%TRAPEZIO)
print("QUADRADO: %0.3f"%QUADRADO)
print("RETANGULO: %0.3f"%DISHA)